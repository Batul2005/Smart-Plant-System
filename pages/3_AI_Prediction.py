"""
pages/3_AI_Prediction.py
--------------------------
Displays ML-based predictions (health score, growth rate, days to
flowering) from the trained Random Forest models, alongside the models'
real, measured accuracy — shown transparently rather than hidden, since
an honest accuracy disclosure is more credible (and more useful) than a
black box.
"""

import sys
import os
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

import streamlit as st
import plotly.graph_objects as go

from backend import models
from backend.simulation import get_latest_sensor_reading
from ml.predict import predict_plant_outcomes, models_available, get_metrics

st.set_page_config(page_title="AI Prediction", page_icon="🔮", layout="wide")

if st.session_state.get("user") is None:
    st.warning("Please log in first.")
    st.stop()

user = st.session_state.user

with st.sidebar:
    st.markdown(f"### 👤 {user['name']}")
    if st.button("Log Out", use_container_width=True):
        st.session_state.user = None
        st.rerun()

plants = models.get_plants_for_user(user["user_id"])
if not plants:
    st.info("No plants yet. Add one from the **My Plants** page.")
    st.stop()

plant_names = {p["plant_id"]: f"{p['plant_name']} ({p['species']})" for p in plants}
default_id = st.session_state.get("selected_plant_id") or plants[0]["plant_id"]
if default_id not in plant_names:
    default_id = plants[0]["plant_id"]

selected_id = st.sidebar.selectbox(
    "Select plant", options=list(plant_names.keys()),
    format_func=lambda pid: plant_names[pid],
    index=list(plant_names.keys()).index(default_id),
)

plant = models.get_plant_by_id(selected_id)
st.title(f"🔮 AI Prediction — {plant['plant_name']}")

if not models_available():
    st.error(
        "ML models haven't been trained yet. Run these from the project root:\n\n"
        "```\npython ml/generate_dataset.py\npython ml/train_model.py\n```"
    )
    st.stop()

reading = get_latest_sensor_reading(selected_id)

st.caption(
    "Predictions are generated by a **Random Forest Regressor** (scikit-learn) trained on "
    "15,000 simulated samples across all supported species, using the same physiological "
    "rules as the live health engine plus realistic sensor/biological noise."
)

prediction = predict_plant_outcomes(
    species=plant["species"],
    water_level=reading["water_level"],
    sunlight=reading["sunlight"],
    temperature=reading["temperature"],
    humidity=reading["humidity"],
    fertilizer=reading["fertilizer"],
)

models.store_prediction(
    selected_id, prediction["predicted_health_score"], "N/A",
    prediction["predicted_growth_rate"], prediction["predicted_days_to_flowering"],
)

col1, col2, col3 = st.columns(3)

with col1:
    fig = go.Figure(go.Indicator(
        mode="gauge+number",
        value=prediction["predicted_health_score"],
        title={"text": "Predicted Health Score"},
        gauge={"axis": {"range": [0, 100]}, "bar": {"color": "#16a34a"}},
    ))
    fig.update_layout(height=260, margin=dict(l=10, r=10, t=50, b=10))
    st.plotly_chart(fig, use_container_width=True)

with col2:
    st.metric("Predicted Growth Rate", f"{prediction['predicted_growth_rate']:.2f}",
               help="Estimated growth-stage progress per simulation cycle")
    st.metric("Days to Flowering", f"{prediction['predicted_days_to_flowering']:.0f} days",
               help="Estimated days until the plant reaches the Flowering stage at current conditions")

with col3:
    st.markdown("**Current inputs used:**")
    st.write(f"💧 Water: {reading['water_level']:.0f}%")
    st.write(f"☀️ Sunlight: {reading['sunlight']:.0f}%")
    st.write(f"🌡️ Temp: {reading['temperature']:.0f}°C")
    st.write(f"💨 Humidity: {reading['humidity']:.0f}%")
    st.write(f"🌱 Fertilizer: {reading['fertilizer']:.0f}%")

st.divider()

# ---------- "What-if" simulator ----------
st.subheader("🧪 What-if Simulator")
st.caption("Adjust the sliders to see how predicted outcomes change under different conditions.")

c1, c2, c3, c4, c5 = st.columns(5)
sim_water = c1.slider("Water %", 0, 100, int(reading["water_level"]))
sim_sun = c2.slider("Sunlight %", 0, 100, int(reading["sunlight"]))
sim_temp = c3.slider("Temp °C", 0, 50, int(reading["temperature"]))
sim_hum = c4.slider("Humidity %", 0, 100, int(reading["humidity"]))
sim_fert = c5.slider("Fertilizer %", 0, 100, int(reading["fertilizer"]))

whatif = predict_plant_outcomes(plant["species"], sim_water, sim_sun, sim_temp, sim_hum, sim_fert)

wc1, wc2, wc3 = st.columns(3)
wc1.metric("What-if Health Score", f"{whatif['predicted_health_score']:.1f}",
           delta=round(whatif['predicted_health_score'] - prediction['predicted_health_score'], 1))
wc2.metric("What-if Growth Rate", f"{whatif['predicted_growth_rate']:.2f}",
           delta=round(whatif['predicted_growth_rate'] - prediction['predicted_growth_rate'], 2))
wc3.metric("What-if Days to Flower", f"{whatif['predicted_days_to_flowering']:.0f}",
           delta=round(whatif['predicted_days_to_flowering'] - prediction['predicted_days_to_flowering'], 1),
           delta_color="inverse")

st.divider()

# ---------- Model transparency ----------
with st.expander("📈 Model accuracy & methodology (real, measured metrics)"):
    metrics = get_metrics()
    st.markdown(
        "Each target is predicted by its own `RandomForestRegressor` (200 trees, max depth 14), "
        "evaluated on a held-out 20% test split of the 15,000-row synthetic dataset:"
    )
    if metrics:
        mc1, mc2, mc3 = st.columns(3)
        for col, (target, m) in zip([mc1, mc2, mc3], metrics.items()):
            with col:
                st.metric(target.replace("_", " ").title(), f"R² = {m['R2']}")
                st.caption(f"MAE = {m['MAE']}")
    st.markdown(
        "**R² (coefficient of determination)** measures how much of the variance in the target "
        "the model explains (1.0 = perfect, 0 = no better than predicting the mean). "
        "**MAE (mean absolute error)** is the average prediction error in the target's own units."
    )
    st.caption(
        "Training data is synthetic, generated from the same domain rules used in the live "
        "health engine plus added noise — see `ml/generate_dataset.py` for full methodology."
    )
